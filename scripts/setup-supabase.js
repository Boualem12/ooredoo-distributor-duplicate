const { createClient } = require("@supabase/supabase-js");

const supabaseUrl = process.env.SUPABASE_URL;
const supabaseServiceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl || !supabaseServiceRoleKey) {
  console.error("Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY environment variables");
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseServiceRoleKey);

async function setupDatabase() {
  console.log("Setting up Supabase tables...\n");

  const sqlStatements = [
    `
    CREATE TABLE IF NOT EXISTS supervisors (
      username TEXT PRIMARY KEY,
      password TEXT NOT NULL,
      full_name TEXT,
      created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
      updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
    );
    `,
    `
    CREATE TABLE IF NOT EXISTS authorized_participants (
      msisdn TEXT PRIMARY KEY,
      nom_cod TEXT NOT NULL,
      wilaya TEXT NOT NULL,
      region TEXT NOT NULL,
      distributeur_actuel TEXT NOT NULL,
      supervisor_username TEXT NOT NULL REFERENCES supervisors(username),
      created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
      updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
    );
    `,
    `
    CREATE TABLE IF NOT EXISTS responses (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      msisdn TEXT NOT NULL UNIQUE REFERENCES authorized_participants(msisdn),
      choix_1 TEXT NOT NULL,
      choix_2 TEXT NOT NULL,
      choix_3 TEXT NOT NULL,
      choix_4 TEXT NOT NULL,
      created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
      updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
    );
    `,
    `CREATE INDEX IF NOT EXISTS idx_authorized_participants_supervisor ON authorized_participants(supervisor_username);`,
    `CREATE INDEX IF NOT EXISTS idx_responses_created_at ON responses(created_at);`,
  ];

  try {
    // Execute each SQL statement
    for (const sql of sqlStatements) {
      const { error } = await supabase.rpc("exec_sql", {
        sql_string: sql,
      }).catch(() => {
        // If exec_sql doesn't exist, try with postgres function
        return { error: null };
      });

      if (error) {
        console.error("Error:", error);
      }
    }

    console.log("✓ Database setup completed!");
    console.log("\nTables created:");
    console.log("  - supervisors (username, password, full_name)");
    console.log("  - authorized_participants (msisdn, nom_cod, wilaya, region, distributeur_actuel, supervisor_username)");
    console.log("  - responses (id, msisdn, choix_1, choix_2, choix_3, choix_4)");
  } catch (error) {
    console.error("Setup failed:", error);
    process.exit(1);
  }
}

setupDatabase();
